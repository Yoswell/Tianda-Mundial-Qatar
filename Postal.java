package tarea1_mundialdqatar;

public class Postal {
        int contador;
        int precio;
        int costoSolcitado;
        String descripcion;
        String postales;

    public int getContador() {
        return contador;
    }

    public void setContador(int contador) {
        this.contador = contador;
    }

    public int getPrecio() {
        return precio;
    }

    public void setPrecio(int precio) {
        this.precio = precio;
    }

    public int getCostoSolcitado() {
        return costoSolcitado;
    }

    public void setCostoSolcitado(int costoSolcitado) {
        this.costoSolcitado = costoSolcitado;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getPostales() {
        return postales;
    }

    public void setPostales(String postales) {
        this.postales = postales;
    }

        
}